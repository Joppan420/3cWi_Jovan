package at.ran.schoolcheck.Test2;

public class WaterTank {
    private int water;
    private int volume;

    public WaterTank(int water, int volume) {
        this.water = water;
        this.volume = volume;
    }

    public int getWater() {
        return water;
    }

    public void setWater(int water) {
        this.water = water;
    }

    public int getVolume() {
        return volume;
    }

    public void setVolume(int volume) {
        this.volume = volume;
    }
}
